    private void btnCompilarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCompilarActionPerformed
        // TODO add your handling code here:
        double acumulador = 0;
        ArrayList<String> nombres = new ArrayList<>();
        ArrayList<Character> tipos = new ArrayList<>();
        ArrayList<String> valores = new ArrayList<>();
        ArrayList<String> lineasEnumeradas = new ArrayList<>();
        
        Compilacion espambler = new Compilacion();
        
        String codigo = txtCodigo.getText();
        
        acumulador = espambler.compilar(codigo, nombres, tipos, valores,lineasEnumeradas);
        
        String acumuladorS = acumulador+"";
        
        txtAcumulador.setText(acumuladorS);
        
        String variable = "";
        
        for (int i = 0; i < nombres.size(); i++){
            variable += nombres.get(i) + " " + tipos.get(i) + " " + valores.get(i) + "\n";
        }
        
        String codigoEnumerado = espambler.imprimir(lineasEnumeradas);
        
        txtVariables.setText(variable);
        txtCodigo.setText(codigoEnumerado);
    }//GEN-LAST:event_btnCompilarActionPerformed

